package ch04.lecture;

public class C01if {
    public static void main(String[] args) {
        // if: 조건문(분기문, branch, condition)
        // () 가 true면 코드블럭실행

        if (true) {
            System.out.println("실행되는 코드!!!");
        }
        if (false) {
            System.out.println("실행되지 않는 코드@@@@@");
        }
        // truthy, falsy 없음

    }
}
